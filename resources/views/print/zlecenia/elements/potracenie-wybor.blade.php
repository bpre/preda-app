    <p>
        <strong>
        Przykład 1: kredytobiorca składa oświadczenie o potrąceniu.
        </strong>
    </p>
    <p>
        Załóżmy, że bank wypłacił kredyt w kwocie 200.000 zł. Kredytobiorca dotychczas zapłacił bankowi 300.000 zł. Złożenie oświadczenia o potrąceniu powoduje, że wierzytelność banku o zwrot kapitału zostaje umorzona (kredytobiorca już na wstępie jest rozliczony z bankiem), a w toku sprawy sądowej kredytobiorca dochodzi zasądzenia kwoty pozostałej po potrąceniu, czyli 100.000 zł. Jeżeli sąd uzna umowę za nieważną - powinien zasądzić na rzecz kredytobiorcy 100.000 zł oraz odsetki od kwoty 100.000 zł.
    </p>

    <p>
        <strong>
        Przykład 2: kredytobiorca nie składa oświadczenia o potrąceniu.
        </strong>
    </p>
    <p>
        To samo założenie: bank wypłacił kredyt w kwocie 200.000 zł. Kredytobiorca dotychczas zapłacił bankowi 300.000 zł. Kredytobiorca nie składa jednak oświadczenia o potrąceniu i w postępowaniu sądowym dochodzi zapłaty kwoty 300.000 zł (oraz odsetek od tej kwoty). Jeżeli sąd uzna umowę za nieważną i zastosuje „teorię dwóch kondykcji” - powinien zasądzić na rzecz kredytobiorcy 300.000 zł oraz odsetki od kwoty 300.000 zł. Nie zwalnia to jednak kredytobiorcy z obowiązku rozliczenia się z bankiem i zapłaty na rzecz banku kwoty 200.000 zł.
    </p>
    <p>
        Jednakże sąd może częściowo oddalić powództwo kredytobiorcy stosując tzw. „teorię salda” i wówczas zasądzi kwotę 100.000 zł (z odsetkami), a przy tym może obciążyć kredytobiorcę kosztami postępowania (od kilku do nawet kilkudziesięciu tysięcy złotych).
    </p>
    <p>
        Ponadto w tym wariancie jest zdecydowanie większe prawdopodobieństwo, że bank złoży pozew przeciwko kredytobiorcy o zwrot kapitału (zapłatę kwoty 200.000 zł), zwłaszcza jeśli postępowanie będzie trwało długo, a bank będzie obawiał się przedawnienia swoich roszczeń.
    </p>


    </p>
    <p>
        Dochodzenie roszczeń po uprzednim złożeniu oświadczenia o potrąceniu pozwala uzyskać niższe odsetki za opóźnienie, ale generalnie jest bezpieczniejsze (mniejsze ryzyko częściowego przegrania sprawy, mniejsze ryzyko obciążenia kredytobiorcy kosztami postępowania, mniejsze prawdopodobieństwo pozwania kredytobiorcy przez bank, większa przewidywalność rozstrzygnięcia).
